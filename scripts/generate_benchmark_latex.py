"""Generate a Beamer appendix .tex from benchmark runs.json + judgements.json.

The appendix slots into slides/main.tex after \\appendix and renders each of
the 30 Q&A side-by-side, with auto-pagination via [allowframebreaks].
"""

from __future__ import annotations

import json
from pathlib import Path

ROOT = Path(__file__).resolve().parent.parent
RUNS = ROOT / "artifacts/benchmark/runs.json"
JUDG = ROOT / "artifacts/benchmark/judgements.json"
QS = ROOT / "artifacts/benchmark/questions.json"
OUT = ROOT / "slides/sections/18_appendix_benchmark.tex"


def tex_escape(s: str) -> str:
    """Escape LaTeX specials. Order matters: \\ first to avoid double-escape."""
    if s is None:
        return ""
    out = []
    for ch in s:
        if ch == "\\":
            out.append("\\textbackslash{}")
        elif ch == "{":
            out.append("\\{")
        elif ch == "}":
            out.append("\\}")
        elif ch == "#":
            out.append("\\#")
        elif ch == "_":
            out.append("\\_")
        elif ch == "&":
            out.append("\\&")
        elif ch == "%":
            out.append("\\%")
        elif ch == "$":
            out.append("\\$")
        elif ch == "^":
            out.append("\\^{}")
        elif ch == "~":
            out.append("\\~{}")
        else:
            out.append(ch)
    return "".join(out)


def render_answer(text: str) -> str:
    """Turn raw model output into Beamer-safe LaTeX paragraphs.

    - Replace `##begin_quote##...##end_quote##` with `「...」` inline marks.
    - Escape LaTeX specials.
    - Convert `\\n\\n` paragraph breaks into LaTeX `\\par`.
    - Convert single `\\n` into Beamer-friendly `\\\\` line breaks.
    - Trim trailing newlines.
    """
    if not text:
        return "\\emph{(空)}"

    # Replace quote markers BEFORE escaping so the # chars don't get mangled.
    text = text.replace("##begin_quote##", "「").replace("##end_quote##", "」")

    # Split into paragraphs, escape per-paragraph, re-join with \par.
    paragraphs = text.split("\n\n")
    out_paras: list[str] = []
    for p in paragraphs:
        p = p.strip("\n")
        if not p:
            continue
        # Line breaks inside a paragraph → \\
        lines = [tex_escape(line) for line in p.split("\n")]
        out_paras.append(" \\\\\n".join(lines))
    return "\n\n\\par\\smallskip\n\n".join(out_paras)


def winner_zh(w: str | None) -> str:
    return {"a": "codex 胜", "b": "qwen 胜", "tie": "平局"}.get(w or "", "?")


def chapter_label(ch: str) -> str:
    mapping = {
        "ch1": "ch1 · 课程引言",
        "ch3": "ch3 · HMM / 马尔科夫",
        "ch4": "ch4 · 经典 ML",
        "ch4(2)": "ch4-2 · 神经网络",
        "ch9": "ch9 · 语言模型",
    }
    return mapping.get(ch, ch)


CHAPTER_ORDER = ["ch1", "ch3", "ch4", "ch4(2)", "ch9"]


def main():
    questions = json.loads(QS.read_text(encoding="utf-8"))["questions"]
    runs = {r["id"]: r for r in json.loads(RUNS.read_text(encoding="utf-8"))}
    judges = {j["id"]: j["judgement"]
              for j in json.loads(JUDG.read_text(encoding="utf-8"))}

    # Group questions by chapter.
    by_ch: dict[str, list[dict]] = {ch: [] for ch in CHAPTER_ORDER}
    for q in questions:
        ch = q.get("chapter") or "?"
        by_ch.setdefault(ch, []).append(q)

    # ── Header / title / summary ──
    parts: list[str] = []
    parts.append(r"""% =====================================================
% §18 附录 · Codex vs Qwen-RAFT 30 题逐题对比
% (auto-generated by scripts/generate_benchmark_latex.py)
% =====================================================

% 答案"软容器" — 用 \colorbox 做单行标题 + 横线 + 自由流动文本。
% beamercolorbox 是刚性容器，allowframebreaks 不会分页它，长答案会被裁掉。
% 这里改成纯文本流，让 Beamer 的 allowframebreaks 自由把内容拆到多张物理 slide。
\newcommand{\benchanswerhead}[3]{%
  \par\smallskip
  \noindent\colorbox{#1}{\color{white}\strut\hspace{0.4em}\textbf{\small #2}\hspace{0.4em}}\par
  \nopagebreak
  {\color{#1}\rule{\linewidth}{0.4pt}}\par
  \nopagebreak\vspace{-2pt}
  \small
  \par\noindent
}
\newcommand{\benchanswerfoot}[1]{%
  \par\vspace{2pt}
  {\color{#1}\rule{\linewidth}{0.4pt}}\par
  \normalsize
}
\newenvironment{codexbox}[1]{%
  \benchanswerhead{deepblue}{codex (GPT-5.4) \textbar{} #1}{}%
}{%
  \benchanswerfoot{deepblue}%
}
\newenvironment{qwenbox}[1]{%
  \benchanswerhead{mypurple}{Qwen2.5-7B-RAFT \textbar{} #1}{}%
}{%
  \benchanswerfoot{mypurple}%
}

% Cover frame for the appendix.
{
\setbeamercolor{background canvas}{bg=deepblue}
\setbeamercolor{normal text}{fg=white}
\begin{frame}[plain]
  \begin{center}
    \color{white}
    \vspace{1cm}
    {\Huge\textbf{附录}}\\[0.6em]
    {\LARGE codex (GPT-5.4) \textit{vs} Qwen2.5-7B-RAFT}\\[0.4em]
    {\Large 30 题逐题对比}\\[2em]
    {\large
      \begin{tabular}{ll}
        题库   & 30 题 \textbar{} 5 章 \textbar{} 中英混合 \textbar{} 4 种题型 \\
        课程   & \textsc{test-slides}（326 chunks, 220 KG nodes）\\
        Qwen   & AutoDL RTX 5090 \textbar{} \textsc{serve\_openai.py} :8001 \\
        Codex  & codex.ysaikeji.cn/v1 \textbar{} GPT-5.4 \\
        裁判   & GPT-5.4 LLM-as-judge \textbar{} accuracy + completeness \\
      \end{tabular}
    }\\[2em]
    {\small\color{lightgray}\textit{2026-05-12 \textbar{} R4-5 fix-all v3 protocol alignment}}
  \end{center}
\end{frame}
}

\section*{附录 · 30 题对比}

% ── 总览表 ──
\begin{frame}{附录 \textbar{} 总览}
\small
\centering
\begin{tabular}{lcc}
\toprule
\textbf{指标} & \textbf{codex (GPT-5.4)} & \textbf{Qwen2.5-7B-RAFT} \\
\midrule
平均响应时长          & 22.49s            & \hl{5.01s}        \\
中位响应时长          & 11.63s            & 4.63s             \\
最长 / 最短           & 180.01s / 5.27s   & 8.03s / 2.00s     \\
平均答案字符数        & 347               & 515               \\
失败 / 错误次数       & 1 (180s 超时空答) & \hl{0}            \\
backend\_fallback     & —                 & 0                 \\
平均 accuracy (0-5)   & 1.97              & \hl{3.30}         \\
平均 completeness (0-5) & 1.83            & \hl{2.97}         \\
\midrule
\textbf{Head-to-head} & 胜 8 / 27\%        & \hl{胜 17 / 57\%} \\
                      &                    & 平 5 / 17\%       \\
\bottomrule
\end{tabular}

\vspace{0.6em}
\footnotesize
\textbf{核心发现：}codex 在 RAG 命中弱时倾向"拒答"（14 题 0 分），
qwen-RAFT 微调过会用预训练知识填空答出标准教材答案；codex 在长结构化对比题和英文题上仍有优势。
\end{frame}

""")

    # ── Per-chapter sections ──
    for ch in CHAPTER_ORDER:
        qs = by_ch.get(ch) or []
        if not qs:
            continue
        parts.append(f"\n\\subsection*{{{tex_escape(chapter_label(ch))}}}\n")
        for q in qs:
            qid = q["id"]
            qtext = q["q"]
            qtype = q.get("type", "?")
            r = runs[qid]
            j = judges.get(qid, {})
            codex_ans = r["codex"]["answer"]
            qwen_ans = r["qwen"]["answer"]
            codex_dur = r["codex"]["duration_s"]
            qwen_dur = r["qwen"]["duration_s"]
            codex_len = len(codex_ans)
            qwen_len = len(qwen_ans)

            a = j.get("a", {}) if isinstance(j, dict) else {}
            b = j.get("b", {}) if isinstance(j, dict) else {}
            winner = j.get("winner") if isinstance(j, dict) else None
            case_note = j.get("case_note") if isinstance(j, dict) else None
            codex_acc = a.get("accuracy", "?")
            qwen_acc = b.get("accuracy", "?")
            codex_comp = a.get("completeness", "?")
            qwen_comp = b.get("completeness", "?")
            codex_notes = a.get("notes") or ""
            qwen_notes = b.get("notes") or ""

            # winner color helper
            if winner == "a":
                winner_tex = r"\textcolor{deepblue}{\textbf{codex 胜}}"
            elif winner == "b":
                winner_tex = r"\textcolor{mypurple}{\textbf{qwen 胜}}"
            elif winner == "tie":
                winner_tex = r"\textbf{平局}"
            else:
                winner_tex = "?"

            # truncate question text for frame title
            ftitle_q = qtext if len(qtext) <= 40 else qtext[:37] + "…"

            parts.append(rf"""
\begin{{frame}}[allowframebreaks]{{{tex_escape(qid)} \textbar{{}} {tex_escape(qtype)}}}
\small
\textbf{{题目：}}{tex_escape(qtext)}

\vspace{{0.4em}}
\footnotesize
\textbf{{裁判：}}{winner_tex}\quad
accuracy(codex/qwen) = {codex_acc}/{qwen_acc}\quad
completeness(codex/qwen) = {codex_comp}/{qwen_comp}\\
\textbf{{延迟：}}codex {codex_dur:.2f}s ({codex_len} 字)\quad
qwen {qwen_dur:.2f}s ({qwen_len} 字)
""")
            if case_note:
                parts.append(f"\\\\\n\\textbf{{评语：}}{tex_escape(case_note)}\n")
            parts.append("\\normalsize\n\\vspace{0.5em}\n\n")

            parts.append(f"\\begin{{codexbox}}{{{codex_dur:.2f}s · {codex_len} 字}}\n")
            parts.append(render_answer(codex_ans))
            parts.append("\n\\end{codexbox}\n\n\\vspace{0.4em}\n\n")

            parts.append(f"\\begin{{qwenbox}}{{{qwen_dur:.2f}s · {qwen_len} 字}}\n")
            parts.append(render_answer(qwen_ans))
            parts.append("\n\\end{qwenbox}\n\n")

            if codex_notes or qwen_notes:
                parts.append("\\vspace{0.3em}\n\\footnotesize\n")
                if codex_notes:
                    parts.append(f"\\textbf{{judge 对 codex：}}{tex_escape(codex_notes)}\\\\\n")
                if qwen_notes:
                    parts.append(f"\\textbf{{judge 对 qwen：}}\\hspace{{0.4em}}{tex_escape(qwen_notes)}\n")
                parts.append("\\normalsize\n")
            parts.append("\\end{frame}\n")

    # Tail frame.
    parts.append(r"""
% ── 闭尾 ──
{
\setbeamercolor{background canvas}{bg=deepblue}
\setbeamercolor{normal text}{fg=white}
\begin{frame}[plain]
  \begin{center}
    \color{white}
    \vspace{2cm}
    {\Huge\textbf{End of Appendix}}\\[1.5em]
    {\large 详细数据 + 题库 + 评分 JSON 见}\\[0.5em]
    {\normalsize\texttt{benchmarks/} \textbar{} \texttt{artifacts/benchmark/}}
  \end{center}
\end{frame}
}
""")

    OUT.write_text("".join(parts), encoding="utf-8")
    print(f"Wrote {OUT} ({OUT.stat().st_size} bytes)")


if __name__ == "__main__":
    main()
